## 1. Python if statement
# Example 1- first example
age = 20                             # stores the integer value 20 in variable age
if age >= 18:                        # checks whether age is greater than or equal to 18
    print("You are an adult.")       # prints this message because age is 20
# Example 2
temperature = 35                     # stores the integer value 35 in variable temperature
if temperature > 30:                 # checks whether temperature is greater than 30
    print("It's a hot day.")         # prints this message because temperature is 35
# Example 3
marks = 85                           # stores the integer value 85 in variable marks
if marks >= 80:                      # checks whether marks are greater than or equal to 80
    print("Grade A")                 # prints Grade A because marks are 85
# Example 4
number = 10                          # stores the integer value 10 in variable number
if number > 0:                       # checks whether number is greater than 0
    print("Positive Number")         # prints this message because number is positive
# Example 5
age = 25                             # stores the integer value 25 in variable age
salary = 50000                       # stores the integer value 50000 in variable salary
if age >= 18 and salary >= 30000:    # checks whether age is at least 18 and salary is at least 30000
    print("Loan Approved")           # prints this message because both conditions are True
# Example 6
balance = 10000                      # stores the integer value 10000 in variable balance
withdraw = 3000                      # stores the integer value 3000 in variable withdraw
if balance >= withdraw:              # checks whether balance is greater than or equal to withdraw amount
    print("Transaction Successful")  # prints this message because balance is enough
