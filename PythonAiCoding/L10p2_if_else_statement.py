# 2. if_else_statement
age = 18                          # Assigns the value 18 to variable age
if age >= 18:                     # Checks if age is greater than or equal to 18
    print("you are an adult!")    # Executes if the condition is True
else:                             # Executes if the condition is False
    print("you are not an adult!")# Prints if age is less than 18

marks = 45                        # Assigns the value 45 to variable marks
if marks >= 50:                   # Checks if marks are 50 or more
    print("you passed the test")  # Executes if marks are >= 50
else:                             # Executes if marks are less than 50
    print("you failed the test")  # Prints this because 45 is less than 50

username = "admin"                # Assigns the string "admin" to variable username
if username == "admin":           # Checks if username is exactly equal to "admin"
    print("Access granted")       # Executes if the username matches "admin"
else:                             # Executes if the username does not match
    print("Access denied")        # Prints if the username is not "admin"



 
